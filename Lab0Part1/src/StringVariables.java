
public class StringVariables {

	public static void main(String[] args) {
		String firstName = "";
		String lastName = "";
		int i = 0;
		
		System.out.println("Person " + i + ": " +firstName+" " +lastName);

		firstName = "Nourin";
		lastName = "Abd El Hadi";
		i += 1;
		
		System.out.println("Person " + i + ": " +firstName+" " +lastName);
		
	}

}
