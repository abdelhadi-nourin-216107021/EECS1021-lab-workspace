		System.out.println("15%4 gives the remainder" + (15%4));
		
		System.out.println("23/4 gives the quotient" + (23/4));
		
		System.out.println("15%4 gives the remainder" + (23%4));
		
		System.out.println("divisor * quotient + remainder = og number:" + (((4*(23/4)+(23%4)))));
		
		System.out.println("1 + 1");
